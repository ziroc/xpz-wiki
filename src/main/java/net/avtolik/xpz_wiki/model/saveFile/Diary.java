package net.avtolik.xpz_wiki.model.saveFile;

public class Diary {
	int daysWoundedTotal;
	int monthsService;
	int timesWoundedTotal;
	int statGainTotal;
	int braveryGainTotal;
	
	public int getDaysWoundedTotal() {
		return daysWoundedTotal;
	}
	public void setDaysWoundedTotal(int daysWoundedTotal) {
		this.daysWoundedTotal = daysWoundedTotal;
	}
	public int getMonthsService() {
		return monthsService;
	}
	public void setMonthsService(int monthsService) {
		this.monthsService = monthsService;
	}
	public int getTimesWoundedTotal() {
		return timesWoundedTotal;
	}
	public void setTimesWoundedTotal(int timesWoundedTotal) {
		this.timesWoundedTotal = timesWoundedTotal;
	}
	public int getStatGainTotal() {
		return statGainTotal;
	}
	public void setStatGainTotal(int statGainTotal) {
		this.statGainTotal = statGainTotal;
	}
	public int getBraveryGainTotal() {
		return braveryGainTotal;
	}
	public void setBraveryGainTotal(int braveryGainTotal) {
		this.braveryGainTotal = braveryGainTotal;
	}

}
